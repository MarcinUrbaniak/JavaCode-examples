public class Person {

    private double height;
    private double weight;
    private int age;
    private String hairColor;
    private SkinColor skinColor;




    public Person(double height, double weight, int age, String hairColor, SkinColor skinColor) {
        this.height = height;
        this.weight = weight;
        this.age = age;
        this.hairColor = hairColor;
        this.skinColor = skinColor;
    }
}


